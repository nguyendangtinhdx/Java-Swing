    public void Biding(){
        ListEMP = empDll.getALL();
        Vector clums = new Vector();
        clums.add("Username");
        clums.add("Password");
        clums.add("Age");
        clums.add("Address");
        clums.add("Phone");
        clums.add("Department");
        clums.add("Project");
        
        Vector data = new Vector();
        for (Employees emp : ListEMP) {
            Vector row = new Vector();
            row.add(emp.getUsername());
            row.add(emp.getPassword());
            row.add(emp.getAge());
            row.add(emp.getAddress());
            row.add(emp.getPhone());
        //    row.add(emp.getIDDep());
         //   row.add(emp.getIDPro());
            listDep = depDLL.getByID(emp.getIDDep());
            if (listDep.size()>0) {
                row.add(listDep.get(0).getNameDep());
            }else{
                row.add("isEmpty");
            }
            listPro = proDLL.getByID(emp.getIDPro());
            if (listPro.size()>0) {
                row.add(listPro.get(0).getNamePro());
            }else{
                row.add("isEmpty");
            }
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data,clums);
        tblEmp.setModel(dtm);
    }
            
