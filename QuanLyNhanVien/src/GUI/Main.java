    private void tblEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpMouseClicked
        if (tblEmp.getSelectedRow() != -1) {
        int row = tblEmp.getSelectedRow();
        listDep = depDLL.getAll();
        listPro = proDLL.getAll();
        ListEMP = empDll.getALL();
        txtUsername.setText(ListEMP.get(row).getUsername());
        txtPassword.setText(ListEMP.get(row).getPassword());
        txtAge.setText(String.valueOf(ListEMP.get(row).getAge())); // chuy·ªÉn ki·ªÉu qua Integer
        txtAddress.setText(ListEMP.get(row).getAddress());
        txtPhone.setText(ListEMP.get(row).getPhone());
         
        listDep = depDLL.getByID(ListEMP.get(row).getIDDep());
        if(listDep.size() > 0){
            cbxDep.setSelectedItem(listDep.get(0).getNameDep());
        }else{
            cbxDep.setSelectedItem(0);
        }
        listPro = proDLL.getByID(ListEMP.get(row).getIDPro());
        if(listPro.size() > 0){
            cbxPro.setSelectedItem(listPro.get(0).getNamePro());
        }else{
            cbxPro.setSelectedItem(0);
        }
        }
    }//GEN-LAST:event_tblEmpMouseClicked
