    private void loadTable(List<Product> readAll) {
        Vector col = new Vector();
        col.add("ProductID");
        col.add("Name");
        col.add("ProducerID");
        col.add("Price");
        col.add("Quantity");
        Vector rows = new Vector();
        for (Product p : readAll) {
            Vector row = new Vector();
            row.add(p.getProductid());
            row.add(p.getName());
            row.add(p.getProducerid());
            row.add(p.getPrice());
            row.add(p.getQuantity());
            rows.add(row);
        }
        tblProduct.setModel(new DefaultTableModel(rows, col));
        tblProduct.updateUI();
        jScrollPane1.setViewportView(this.tblProduct);
    }
