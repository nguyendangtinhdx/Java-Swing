    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblProduct.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn dòng để xóa!");
        } else {
            int productid = Integer.parseInt(tblProduct.getValueAt(index, 0).toString());
            if (actions.delete(productid)) {
                JOptionPane.showMessageDialog(this, "Successfull");
                loadTable(actions.readAll());
            } else {
                JOptionPane.showMessageDialog(this, "fail");
            }
        }

    }//GEN-LAST:event_btnXoaActionPerformed

