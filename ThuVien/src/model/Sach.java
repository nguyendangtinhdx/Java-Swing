package model;

import controller.GiaoTiep;
import java.io.Serializable;

/**
 *
 * @author Nguyen Dang Tinh
 */
public class Sach implements Serializable, GiaoTiep{
    private int masach,namxb;
    private String tensach,theloai,tentg,vitri;
    @Override
    public Object[] toObject() {
        return new Object[]{
            this.getMasach(),this.getTensach(),this.getTheloai(),this.getTentg(),this.getNamxb(),this.getVitri()
        };
    }

    public int getMasach() {
        return masach;
    }

    public void setMasach(int masach) {
        this.masach = masach;
    }

    public int getNamxb() {
        return namxb;
    }

    public void setNamxb(int namxb) {
        this.namxb = namxb;
    }

    public String getTensach() {
        return tensach;
    }

    public void setTensach(String tensach) {
        this.tensach = tensach;
    }

    public String getTheloai() {
        return theloai;
    }

    public void setTheloai(String theloai) {
        this.theloai = theloai;
    }

    public String getTentg() {
        return tentg;
    }

    public void setTentg(String tentg) {
        this.tentg = tentg;
    }

    public String getVitri() {
        return vitri;
    }

    public void setVitri(String vitri) {
        this.vitri = vitri;
    }
    
}
